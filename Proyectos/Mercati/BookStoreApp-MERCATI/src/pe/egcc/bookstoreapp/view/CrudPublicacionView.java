  private void cargarDatos() {

    // Acceso al objeto Table
    DefaultTableModel tabla;
    tabla = (DefaultTableModel) tblData.getModel();

    // Eliminar  todas las filas
    tabla.setRowCount(0);

    // Cargar Datos
    for (Publicacion pub : lista) {
      Object[] rowData = {
        pub.getIdpublicacion(),
        pub.getTitulo(),
        pub.getAutor(),
        pub.getNroedicion(),
        pub.getPrecio(),
        pub.getStock()
      };
      tabla.addRow(rowData);
    }

  }

