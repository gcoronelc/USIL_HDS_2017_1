  private void cargarDatos() {
  
    // Acceso al objeto Table
    DefaultTableModel tabla;
    tabla = (DefaultTableModel) tblData.getModel();
    
    // Eliminar  todas las filas
    tabla.setRowCount(0);
    
    // Cargar Datos
    for (Empleado emp : lista) {
      Object[] rowData = {
        emp.getIdempleado(),
        emp.getApellido(),
        emp.getNombre(),
        emp.getEmail()
      };
      tabla.addRow(rowData);
    }
  
  }
