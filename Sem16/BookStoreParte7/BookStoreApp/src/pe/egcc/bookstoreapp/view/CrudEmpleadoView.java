  private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
  
    CrudEmpleadoEditView view;
    view = new CrudEmpleadoEditView(null, true);
    view.setAccion(AppUtil.CRUD_NUEVO);
    
    Session.put("bean", null);
    view.setVisible(true);
    
    if(Session.get("bean") != null){
      Empleado bean = (Empleado) Session.get("bean");
      lista.add(bean);
      cargarDatos();
    }
    
    
  }//GEN-LAST:event_btnNuevoActionPerformed

  private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
    
    int row = tblData.getSelectedRow();
    
    if( row == -1){
      return;
    }
        
    CrudEmpleadoEditView view;
    view = new CrudEmpleadoEditView(null, true);
    view.setAccion(AppUtil.CRUD_EDITAR);
    view.setRowData(lista.get(row));
    
    Session.put("bean", null);
    view.setVisible(true);
    
  }//GEN-LAST:event_btnEditarActionPerformed

